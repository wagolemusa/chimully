                 <h4>Protecting Your Home from Bed Bugs </h4>
                 Bed bugs are great hitchhikers. They can move from an infested site to a new home by traveling on furniture, bedding, luggage, boxes, and clothing. Although they typically feed on blood every five to ten days, bed bugs can be quite resilient; they are capable of surviving several months to a year without feeding. Prevent bed bug infestation in your home: Check secondhand furniture, beds, and couches for any signs of bed bug infestation before bringing them home. Use a protective cover that encases mattresses and box springs and eliminates many hiding spots. The light color of the encasement makes bed bugs easier to see. Be sure to purchase a high quality encasement that will resist tearing and check the encasements regularly for holes. 
                    
                    <h4>Catflea: Silverfish & Firebrats</h4>
                    Reduce clutter in your home to reduce hiding places for bed bugs. Vacuum frequently to remove any successful hitchhikers. Be vigilant when using shared laundry facilities. Transport items to be washed in plastic bags (if you have an active infestation, use a new bag for the journey home). Remove from dryer directly into bag and fold at home. (A dryer on high heat can kill bed bugs.) If you live in a multi-family home, try to isolate your unit by: Installing door sweeps on the bottom of doors to discourage movement into hallways. Sealing cracks and crevices around baseboards, light sockets, etc., to discourage movement through wall voids. Consider purchasing a portable heating chamber to treat any items that you believe may have bed bugs. Be sure to read and carefully follow the directions if you use one of these units and be aware that they are not regulated by EPA or other federal agencies.  
                    
